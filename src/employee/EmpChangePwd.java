    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        char[] oldpwd = jPasswordField1.getPassword();
        String oldpwd1 = new String(oldpwd);
        char[] newpwd = jPasswordField2.getPassword();
        String newpwd1 = new String(newpwd);
        char[] confirmpwd = jPasswordField3.getPassword();
        String confirmpwd1 = new String(confirmpwd);
        
        Validations v = new Validations();
        
        if( !v.passwordValidate(oldpwd1) )
        {
            JOptionPane.showMessageDialog(this, "Old Password is not valid", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if( !v.passwordValidate(newpwd1) )
        {
            JOptionPane.showMessageDialog(this, "New Password is not valid", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if( !v.passwordValidate(confirmpwd1) )
        {
            JOptionPane.showMessageDialog(this, "Confirm Password is not valid", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            
            try(
                    
                    Connection con = DbConnection.getConnect();
                    PreparedStatement ps = con.prepareStatement("select * from register where module='employee' and password = '"+oldpwd1+"'");
                    ResultSet rs = ps.executeQuery();
                    
                )
            {
                if( rs.next() )
                {
                    if( newpwd1.equals(confirmpwd1) )
                    {
                        
                        try(
                                
                                Connection con1 = DbConnection.getConnect();
                                PreparedStatement ps1 = con1.prepareStatement("update register set password = '"+newpwd1+"' where password = '"+oldpwd1+"' and module = 'employee'");
                                
                            )
                        {
                            int i = ps1.executeUpdate();
                            
                            if( i > 0 )
                            {
                                JOptionPane.showMessageDialog(this, "Password changed succesfully");
                                jPasswordField1.setText("");
                                jPasswordField2.setText("");
                                jPasswordField3.setText("");
                                
                                LoginPage lp = new LoginPage();
                                lp.setVisible(true);
                                setVisible(false);
                                
                            }
                            else
                            {
                                JOptionPane.showMessageDialog(this, "Password didn't changed due to some error...!!!", "Error", JOptionPane.ERROR_MESSAGE);
                            }
                            
                        }
                        catch(SQLException ee)
                        {
                            ee.printStackTrace();
                        }
                        
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "New Password didnt matched .", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Old Password didnt matched", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(SQLException e)
            {
                e.printStackTrace();
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

